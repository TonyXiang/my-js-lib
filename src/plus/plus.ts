/**
 * 加法运算，用于两个数相加
 * @function plus
 * @param {number} a - 加法的参数a.
 * @param {number} b - 加法的参数b.
 * @example // returns 2
 * Bxs.plus(1, 1);
 * @returns {number}
 * @throws {} plus方法的参数必须为Number类型
 * @author tony <123456@qq.com>
 */
export default function plus(a: number, b: number): number {
  return a + b
}
